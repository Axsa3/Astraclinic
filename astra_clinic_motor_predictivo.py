"""## 🔧 Paso 4 — Feature Engineering
> Convertimos las variables categóricas en numéricas para el modelo.
"""

df_model = df.copy()

# Encoding de variables categóricas
le = LabelEncoder()
df_model['especialidad_enc'] = le.fit_transform(df_model['especialidad'])
df_model['localidad_enc']    = le.fit_transform(df_model['localidad'])

# One-hot para hora_franja y dia_semana
df_model = pd.get_dummies(df_model, columns=['hora_franja', 'dia_semana'], drop_first=True)

# Variables finales del modelo
FEATURES = [
    'especialidad_enc', 'localidad_enc', 'distancia_km',
    'inasistencias_prev', 'dias_anticipacion', 'pico_y_placa', 'edad',
    'hora_franja_mañana', 'hora_franja_noche',
    'dia_semana_lunes', 'dia_semana_martes', 'dia_semana_miércoles',
    'dia_semana_jueves', 'dia_semana_viernes', 'dia_semana_sábado',
]
# Filtrar solo columnas que existen
FEATURES = [f for f in FEATURES if f in df_model.columns]
TARGET = 'inasistio'

X = df_model[FEATURES]
y = df_model[TARGET]

# División entrenamiento / prueba (80% / 20%)
X_train, X_test, y_train, y_test = train_test_split(
    X, y, test_size=0.20, random_state=42, stratify=y
)

# Normalización
scaler = StandardScaler()
X_train_sc = scaler.fit_transform(X_train)
X_test_sc  = scaler.transform(X_test)

print(f'✅ Feature engineering completado')
print(f'   Variables del modelo : {len(FEATURES)}')
print(f'   Registros entrenamiento: {X_train.shape[0]}')
print(f'   Registros prueba       : {X_test.shape[0]}')
print(f'   Balance objetivo       : {y_train.mean():.1%} inasistencias')

"""## 🤖 Paso 5 — Entrenamiento: Regresión Logística vs Random Forest"""

# ── Modelo 1: Regresión Logística ──
lr = LogisticRegression(max_iter=1000, random_state=42, class_weight='balanced')
lr.fit(X_train_sc, y_train)
y_pred_lr   = lr.predict(X_test_sc)
y_proba_lr  = lr.predict_proba(X_test_sc)[:, 1]

acc_lr = accuracy_score(y_test, y_pred_lr)
f1_lr  = f1_score(y_test, y_pred_lr)
auc_lr = roc_auc_score(y_test, y_proba_lr)

print('📌 REGRESIÓN LOGÍSTICA')
print(f'   Accuracy : {acc_lr:.3f}')
print(f'   F1-Score : {f1_lr:.3f}')
print(f'   AUC-ROC  : {auc_lr:.3f}')
print()

# ── Modelo 2: Random Forest ──
rf = RandomForestClassifier(n_estimators=100, random_state=42,
                             class_weight='balanced', n_jobs=-1)
rf.fit(X_train, y_train)
y_pred_rf   = rf.predict(X_test)
y_proba_rf  = rf.predict_proba(X_test)[:, 1]

acc_rf = accuracy_score(y_test, y_pred_rf)
f1_rf  = f1_score(y_test, y_pred_rf)
auc_rf = roc_auc_score(y_test, y_proba_rf)

print('📌 RANDOM FOREST')
print(f'   Accuracy : {acc_rf:.3f}')
print(f'   F1-Score : {f1_rf:.3f}')
print(f'   AUC-ROC  : {auc_rf:.3f}')

# Seleccionar mejor modelo
mejor_modelo = 'Random Forest' if auc_rf > auc_lr else 'Regresión Logística'
print(f'\n🏆 Mejor modelo por AUC-ROC: {mejor_modelo}')

"""## 📈 Paso 6 — Visualización de resultados"""

fig, axes = plt.subplots(1, 3, figsize=(16, 5))
fig.patch.set_facecolor('#050c14')
fig.suptitle('ASTRA-CLINIC — Evaluación del Modelo Predictivo',
             fontsize=14, fontweight='bold', color=AZUL)

# 1. Curvas ROC
ax = axes[0]
fpr_lr, tpr_lr, _ = roc_curve(y_test, y_proba_lr)
fpr_rf, tpr_rf, _ = roc_curve(y_test, y_proba_rf)
ax.plot(fpr_lr, tpr_lr, color=AZUL,  lw=2, label=f'Log. Reg. (AUC={auc_lr:.2f})')
ax.plot(fpr_rf, tpr_rf, color=VERDE, lw=2, label=f'Rnd. Forest (AUC={auc_rf:.2f})')
ax.plot([0,1],[0,1],'--', color='gray', alpha=0.5, label='Azar')
ax.set_facecolor('#0a1628')
ax.set_title('Curvas ROC', color=AZUL, fontweight='bold')
ax.set_xlabel('Tasa Falsos Positivos', color='white')
ax.set_ylabel('Tasa Verdaderos Positivos', color='white')
ax.tick_params(colors='white')
ax.legend(fontsize=9)

# 2. Matriz de confusión — Random Forest
ax = axes[1]
cm = confusion_matrix(y_test, y_pred_rf)
sns.heatmap(cm, annot=True, fmt='d', ax=ax,
            cmap='Blues', linewidths=0.5,
            xticklabels=['Asistió','No asistió'],
            yticklabels=['Asistió','No asistió'])
ax.set_title('Matriz de Confusión (RF)', color=AZUL, fontweight='bold')
ax.set_xlabel('Predicho', color='white')
ax.set_ylabel('Real', color='white')
ax.tick_params(colors='white')

# 3. Importancia de variables — Random Forest
ax = axes[2]
importancias = pd.Series(rf.feature_importances_, index=FEATURES).sort_values(ascending=True).tail(8)
bars = ax.barh(importancias.index, importancias.values, color=DORADO, alpha=0.85)
ax.set_facecolor('#0a1628')
ax.set_title('Variables más importantes', color=AZUL, fontweight='bold')
ax.tick_params(colors='white')
ax.set_xlabel('Importancia', color='white')

plt.tight_layout()
plt.savefig('resultados_modelo.png', dpi=150, bbox_inches='tight', facecolor='#050c14')
