ax.tick_params(colors='white')
ax.set_facecolor('#0a1628')
for bar, val in zip(bars, tasa_esp.values):
    ax.text(val + 0.005, bar.get_y() + bar.get_height()/2,
            f'{val:.1%}', va='center', color='white', fontsize=9)

# 2. Tasa por franja horaria
ax = axes[0,1]
tasa_hora = df.groupby('hora_franja')['inasistio'].mean()
colores = [VERDE, DORADO, ROJO]
ax.bar(tasa_hora.index, tasa_hora.values, color=colores, alpha=0.85)
ax.set_title('Por franja horaria', color=AZUL, fontweight='bold')
ax.tick_params(colors='white')
ax.set_facecolor('#0a1628')
ax.set_ylabel('Tasa inasistencia', color='white')

# 3. Tasa por localidad
ax = axes[0,2]
tasa_loc = df.groupby('localidad')['inasistio'].mean().sort_values(ascending=True)
ax.barh(tasa_loc.index, tasa_loc.values, color=DORADO, alpha=0.8)
ax.set_title('Por localidad Bogotá', color=AZUL, fontweight='bold')
ax.tick_params(colors='white')
ax.set_facecolor('#0a1628')

# 4. Inasistencias previas vs probabilidad
ax = axes[1,0]
tasa_prev = df.groupby('inasistencias_prev')['inasistio'].mean()
ax.plot(tasa_prev.index, tasa_prev.values, 'o-', color=ROJO, linewidth=2, markersize=8)
ax.fill_between(tasa_prev.index, tasa_prev.values, alpha=0.2, color=ROJO)
ax.set_title('Historial → Probabilidad futura', color=AZUL, fontweight='bold')
ax.set_xlabel('Inasistencias previas', color='white')
ax.tick_params(colors='white')
ax.set_facecolor('#0a1628')

# 5. Pico y placa
ax = axes[1,1]
tasa_pico = df.groupby('pico_y_placa')['inasistio'].mean()
ax.bar(['Sin pico y placa', 'Con pico y placa'], tasa_pico.values,
       color=[VERDE, ROJO], alpha=0.85)
ax.set_title('Impacto pico y placa Bogotá', color=AZUL, fontweight='bold')
ax.tick_params(colors='white')
ax.set_facecolor('#0a1628')
for i, v in enumerate(tasa_pico.values):
    ax.text(i, v + 0.005, f'{v:.1%}', ha='center', color='white', fontweight='bold')

# 6. Distribución días anticipación
ax = axes[1,2]
tasa_anti = df.groupby('dias_anticipacion')['inasistio'].mean()
ax.bar(tasa_anti.index.astype(str), tasa_anti.values, color=AZUL, alpha=0.8)
ax.set_title('Anticipación al agendar', color=AZUL, fontweight='bold')
ax.set_xlabel('Días de anticipación', color='white')
ax.tick_params(colors='white')
